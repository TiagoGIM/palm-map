#!/usr/bin/env node
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')
const DATASET_PATH = resolve(ROOT, 'datasets/recife-v1.documents.json')
const SCHEMA_PATH = resolve(ROOT, 'schema/recife-v1-document.schema.json')
const ARTIFACT_JSON_PATH = resolve(ROOT, 'artifacts/recife-v1.chunks.json')
const ARTIFACT_TS_PATH = resolve(ROOT, 'artifacts/recife-v1.chunks.ts')

const rawDataset = readFileSync(DATASET_PATH, 'utf-8')
const rawSchema = readFileSync(SCHEMA_PATH, 'utf-8')

const dataset = JSON.parse(rawDataset)
const schema = JSON.parse(rawSchema)

if (!Array.isArray(dataset)) {
  throw new Error('Dataset must be an array of documents.')
}

const validationErrors = validateDocuments(dataset, schema)
if (validationErrors.length > 0) {
  const joined = validationErrors.map((error) => `- ${error}`).join('\n')
  throw new Error(`Dataset validation failed:\n${joined}`)
}

const chunks = buildChunks(dataset)
mkdirSync(resolve(ROOT, 'artifacts'), { recursive: true })
writeFileSync(ARTIFACT_JSON_PATH, JSON.stringify(chunks, null, 2))
writeFileSync(ARTIFACT_TS_PATH, toTsModule(chunks))

console.log(
  `[ingest-recife-v1] documents=${dataset.length} chunks=${chunks.length} artifact=${ARTIFACT_JSON_PATH}`,
)

function validateDocuments(documents, parsedSchema) {
  const errors = []
  const required = parsedSchema.required ?? []
  const categories = parsedSchema.properties?.category?.enum ?? []

  documents.forEach((doc, docIndex) => {
    if (!doc || typeof doc !== 'object' || Array.isArray(doc)) {
      errors.push(`doc[${docIndex}] must be an object`)
      return
    }

    required.forEach((field) => {
      if (doc[field] === undefined || doc[field] === null || doc[field] === '') {
        errors.push(`doc[${docIndex}] missing required field: ${field}`)
      }
    })

    if (doc.city !== 'Recife') {
      errors.push(`doc[${docIndex}] city must be Recife`)
    }

    if (!categories.includes(doc.category)) {
      errors.push(`doc[${docIndex}] invalid category: ${String(doc.category)}`)
    }

    if (!Array.isArray(doc.tags) || doc.tags.length === 0) {
      errors.push(`doc[${docIndex}] tags must be a non-empty array`)
    }

    if (typeof doc.updatedAt !== 'string' || !/^\d{4}-\d{2}-\d{2}$/.test(doc.updatedAt)) {
      errors.push(`doc[${docIndex}] updatedAt must match YYYY-MM-DD`)
    }
  })

  return errors
}

function buildChunks(documents) {
  const maxChars = 320
  const overlap = 60
  const chunks = []

  documents.forEach((doc) => {
    const text = normalizeSpaces(`${doc.summary}\n\n${doc.content}`)
    const parts = splitByWindow(text, maxChars, overlap)

    parts.forEach((part, index) => {
      chunks.push({
        chunkId: `${doc.id}-chunk-${index + 1}`,
        docId: doc.id,
        city: doc.city,
        title: doc.title,
        category: doc.category,
        region: doc.region,
        summary: doc.summary,
        content: doc.content,
        snippet: part,
        tags: doc.tags,
        source: doc.source,
        updatedAt: doc.updatedAt,
      })
    })
  })

  return chunks
}

function splitByWindow(text, maxChars, overlapChars) {
  if (text.length <= maxChars) {
    return [text]
  }

  const out = []
  let start = 0

  while (start < text.length) {
    const end = Math.min(start + maxChars, text.length)
    let piece = text.slice(start, end)

    if (end < text.length) {
      const lastBreak = Math.max(piece.lastIndexOf('. '), piece.lastIndexOf('; '))
      if (lastBreak > 100) {
        piece = piece.slice(0, lastBreak + 1)
      }
    }

    out.push(piece.trim())

    if (end >= text.length) {
      break
    }

    const nextStart = start + piece.length - overlapChars
    start = nextStart > start ? nextStart : end
  }

  return out
}

function normalizeSpaces(value) {
  return value.replace(/\s+/g, ' ').trim()
}

function toTsModule(chunks) {
  return [
    '// Auto-generated by scripts/ingest-recife-v1.mjs',
    '// Do not edit manually.',
    '',
    'export type RetrievalChunkArtifact = {',
    '  chunkId: string',
    '  docId: string',
    '  city: string',
    '  title: string',
    "  category: 'attraction' | 'neighborhood' | 'food_cafe' | 'logistics'",
    '  region: string',
    '  summary: string',
    '  content: string',
    '  snippet: string',
    '  tags: string[]',
    '  source: string',
    '  updatedAt: string',
    '}',
    '',
    `export const recifeV1Chunks: RetrievalChunkArtifact[] = ${JSON.stringify(chunks, null, 2)}\n`,
  ].join('\n')
}
